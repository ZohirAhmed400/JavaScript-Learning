// Increment Operator

// In JavaScript we use the increment operator to increase a value stored in a variable. The increment could be pre or post increment.
// Let us see each of them:


// 1- Pre-increment

let count = 1
console.log(++count) //2
console.log(++count) //3
console.log(++count) //4

// 2- Post-increment

let count1 = 1
console.log(count1++) //1
console.log(count1++) //2
console.log(count1++) //3

// Decrement Operator

// In JavaScript we use the decrement operator to decrease a value stored in a variable. The decrement could be pre or post decrement. 
// Let us see each of them:

// 1- Pre-decrement

let count2 = 1
console.log(--count2) //0
console.log(--count2) //-1
console.log(--count2) //-2

// 2- Post-decrement

let count3 = 1
console.log(count3--) //1
console.log(count3--) //0
console.log(count3--) //-1